# --------------
# --Boolean Operator --
# --------------
# and
# or
# not
# ----------------

age = 36
country = "syria"
rank = 10
print(age > 16 and country == "syria" and rank > 7)
print(age > 16 or country == "syria")
print(age > 40 or country == "KSA")
print(age > 40 or country == "KSA" or rank > 7)
print(not age > 16)

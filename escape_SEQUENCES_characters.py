# --------------------------------------
# Escape Sequences Characters
# -------------------------------------

print("hello\bworld")  # will remove o
print("i \
love \
python")  # escape new lin
print("i love python \\")  # escape backslash
print('i love python \'test\' ')  # escape single quote
print("i love python \"test\" ")  # escape double quote
print("hello world \nsecond line ")  # line feed
print("123456789\rAbcde")  # carriage return
print("hello world \tsecond line ")
print(" \x4f\x73 ")

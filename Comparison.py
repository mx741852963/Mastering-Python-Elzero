# --------------
# --Comparison Operators--
# --------------
# [==] Equal
# [!=] Not Equal
# [ > ] Greater than
# [ < ] Less than
# [ >= ] Greater than or Equal
# [ <= ] Less than or Equal
# -----------------------

print(100 == 100)
print(100 == 100.00)
print(100 == 110)
print("=" * 50)
print(100 != 100)
print(100 != 100.00)
print(100 != 110)
print("=" * 50)
print(100 > 100)
print(100 > 100.00)
print(100 > 110)
print("=" * 50)
print(100 < 100)
print(100 < 100.00)
print(100 < 110)
print("=" * 50)
print(100 >= 100)
print(100 >= 100.00)
print(100 >= 110)
print("=" * 50)
print(100 <= 100)
print(100 <= 100.00)
print(100 <= 110)
print("=" * 50)
